package Codes;

public class BitWiseOperator {
    public static void main(String[] args){
        int i = 10;
        System.out.println(i);

        // A single left shift multiplies the number by 2
        int j = i<<1;
        System.out.println(j); // 20

        // A single right shift divides the number by 2
        int k = i>>1;
        System.out.println(k); // 5

    }
}
